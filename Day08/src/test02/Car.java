package test02;

public class Car {
	String Color;
	int Speed;
	int Gear;
	
	public void setColor(String m) {
		Color = m;
	}
	
	public void setSpeed(int m) {
		Speed = m;
	}
	
	public void setGear(int m) {
		Gear = m;
	}
	public String getColor() {
		return Color;
	}
	
	public int getSpeed() {
		return Speed;
	}
	
	public int getGear() {
		return Gear;
	}
}
